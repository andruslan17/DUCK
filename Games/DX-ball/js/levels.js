function buildLevel(game, level) {
    let bricks = [];

    level.forEach((row, rowIndex) => {
        row.forEach((brick, brickIndex) => {
            if (brick === 1) {
                let position = {
                    x: 20 + 70 * brickIndex,
                    y: 75 + 40 * rowIndex
                };
                bricks.push(new Brick(game, position));
            }
        })
    });
    return bricks;
}

